#include<stdio.h>

int main(int argc, char const *argv[])
{
    for (int i = 7; i >= 1; i--)
    {
        for (int a = 0; a < i; a++)
        {
            printf("*");
        }
        for (int b = 0; b < (7-i); b++)
        {
            printf("~");
        }
        printf("\n");
        
    }
    
    return 0;
}
